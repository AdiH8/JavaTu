public class Competitor {
    private int id;
    private String name;
    private int age;
    private double oddToWin;
    private double betToWin;

    public Competitor(int id, String name, int age, double oddToWin, double betToWin) {
        this.id = id;
        this.name = name;
        this.age = age;
        this.oddToWin = oddToWin;
        this.betToWin = betToWin;
    }
}
